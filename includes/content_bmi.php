    <div class="child">
      <p class="header-explain">
        Children's BMI
      </p>
      <p class="pragraph-explain">
        For children and young people aged 2 to 18, the BMI calculator takes into account age and gender as well as
        height and weight.
        Overweight children are thought to be at increased risk of a variety of health conditions, and they're also more
        likely to be overweight as adults.
        The BMI calculator works out if a child or young person is:
        underweight – on the 2nd centile or below
        healthy weight – between the 2nd and 91st centiles
        overweight – 91st centile or above
        very overweight – 98th centile or above
        A child's BMI is expressed as a "centile" to show how their BMI compares with children who took part in national
        surveys.
      </p>
      <p class="header-explain">
        Limitations of the BMI
      </p>
      <p class="pragraph-explain">
        Your BMI can tell you if you're carrying too much weight, but it cannot tell if you're carrying too much fat.
        The BMI cannot tell the difference between excess fat, muscle or bone.
        The adult BMI does not take into account age, gender or muscle mass.
        This means:
        very muscular adults and athletes may be classed "overweight" or "obese" even though their body fat is low
        adults who lose muscle as they get older may fall into the "healthy weight" range even though they may be
        carrying excess fat
        Pregnancy will also affect a woman's BMI result. Your BMI will go up as your weight increases. You should use
        your pre-pregnancy weight when calculating your BMI.
        Apart from these limitations, the BMI is a relatively straightforward and convenient way of assessing someone's
        weight.
      </p>
    </div>
